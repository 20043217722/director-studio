- 现代：包豪斯(形式服从功能/钢+玻璃)/粗野主义(裸露混凝土/块状)/解构主义(非欧几何/碎片)/参数化(算法生成的有机形态)/高技派(暴露结构/管线外置)
- 幻想：赛博朋克(霓虹/巨型屏幕/雨夜/义体)/蒸汽朋克(黄铜/齿轮/铆钉/维多利亚)/柴油朋克(钢铁/焊接/巨型机械)/太阳朋克(植被+科技+可持续)/废土(锈蚀/拼接/匮乏)

